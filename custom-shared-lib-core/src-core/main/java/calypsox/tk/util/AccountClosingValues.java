package calypsox.tk.util;

public class AccountClosingValues {
	
	private boolean accClosing;
	private double closingVal;
	private double openVal;
	
	
	public boolean isAccClosing() {
		return accClosing;
	}
	public void setAccClosing(boolean accClosing) {
		this.accClosing = accClosing;
	}
	public double getClosingVal() {
		return closingVal;
	}
	public void setClosingVal(double closingVal) {
		this.closingVal = closingVal;
	}
	public double getOpenVal() {
		return openVal;
	}
	public void setOpenVal(double openVal) {
		this.openVal = openVal;
	}
	
	

}
